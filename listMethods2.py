print("Using pop: ")
l1 = ["ferrari","Bugatti","Aston Martin","Rolls Royace"]
l1.pop(2)
print(l1)

print("Using remove: ")
l2 = ["ferrari","Bugatti","Aston Martin","Rolls Royace"]
l2.remove("Aston Martin")
print(l2)

print("Using clear: ")
l3 = ["ferrari","Bugatti","Aston Martin","Rolls Royace"]
l3.clear()
print(l3)